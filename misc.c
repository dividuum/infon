#include <stdarg.h>
#include <stdio.h>
#include <stdlib.h>

void die(char *fmt, ...) {
    va_list ap;
    va_start(ap, fmt);
    printf("FATAL: ");
    vprintf(fmt, ap);
    printf("\n");
    va_end(ap);
    exit(1);
}
